"""
base_model.py — Abstract Base Class for All ML/DL Models
==========================================================
Adaptive AI for Cyber Threat Detection

Defines the contract that every threat detection model must implement.
This enforces a consistent interface across all 4 detection modules,
enabling the Threat Fusion Engine (Module 10) to call any model
through a unified API.

Design Patterns:
  - Template Method: base class defines the algorithm skeleton
  - Abstract Factory: each detector module creates its own model variants
  - Strategy: models are interchangeable behind the same interface

SOLID Principles:
  - Interface Segregation: models only implement what they need
  - Dependency Inversion: high-level modules depend on this abstraction
  - Open/Closed: new models extend this base without modifying it

IEEE 29148 FR Reference: FR-MDL-001 through FR-MDL-006

Author: B.Tech Capstone Project
"""

